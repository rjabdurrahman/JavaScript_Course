/* Logical Operators */
// And &&
console.log(1 == 1 && 'a' == 'a')
console.log(4 > 1 && 2 == 3)
// Or ||
console.log(1 == 1 || 'a' == 'a')
console.log(4 > 1 || 2 == 3)
// Not !
console.log(!true)
console.log(!false)
console.log(!(1 == 1))
